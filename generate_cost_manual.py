class CostManual(FPDF):
    def header(self):
        if self.page_no() > 1:
            self.set_font('Loma', '', 7)
            self.set_text_color(150,150,150)
            self.cell(0, 5, 'คู่มือการคํานวณต้นทุนเมนู - ระบบร้านอาหาร BY เต็ง888', 0, 1, 'C')
            self.line(10, 12, 200, 12)
            self.ln(3)

    def footer(self):
        self.set_y(-15)
        self.set_font('Loma', '', 7)
        self.set_text_color(150,150,150)
        self.cell(0, 10, f'หน้า {self.page_no()}/{{nb}}', 0, 0, 'C')

    def chapter_title(self, title):
        self.set_font('Loma', 'B', 16)
        self.set_text_color(212, 175, 55)
        self.cell(0, 10, title, 0, 1, 'L')
        self.set_draw_color(212, 175, 55)
        self.line(10, self.get_y(), 200, self.get_y())
        self.ln(4)

    def section_title(self, title):
        self.set_font('Loma', 'B', 13)
        self.set_text_color(27, 58, 87)
        self.cell(0, 9, title, 0, 1, 'L')
        self.ln(2)

    def body_text(self, text):
        self.set_font('Loma', '', 10)
        self.set_text_color(60, 60, 60)
        self.multi_cell(0, 6, text)
        self.ln(2)

    def bullet(self, text):
        self.set_font('Loma', '', 10)
        self.set_text_color(60, 60, 60)
        x = self.get_x()
        self.cell(6, 6, chr(8226), 0, 0, 'C')
        self.multi_cell(0, 6, text, new_x=XPos.LMARGIN, new_y=YPos.NEXT)

    def info_box(self, text):
        self.set_fill_color(240, 248, 255)
        self.set_draw_color(100, 149, 237)
        self.set_font('Loma', '', 9)
        self.set_text_color(30, 60, 120)
        y_before = self.get_y()
        self.set_x(15)
        self.multi_cell(175, 5.5, text, border=1, fill=True, align='L')
        self.ln(3)

    def formula_box(self, title, formula, example):
        self.set_fill_color(255, 248, 220)
        self.set_draw_color(212, 175, 55)
        self.set_font('Loma', 'B', 10)
        self.set_text_color(139, 105, 20)
        self.set_x(15)
        self.cell(175, 7, title, 0, 1, 'L', fill=True)
        self.set_x(15)
        self.set_font('Loma', '', 10)
        self.set_text_color(60, 60, 60)
        self.multi_cell(175, 6, formula, border=0, fill=True)
        self.set_x(15)
        self.set_font('Loma', '', 8)
        self.set_text_color(100, 100, 100)
        self.multi_cell(175, 5, example, border=1, fill=True)
        self.ln(4)


